// Helper functions for easier data manipulation
export const getNewReleasesByGenre = genre => {
  return newReleasesChannels.filter(channel => channel.genre.toLowerCase() === genre.toLowerCase());
};

export const getNewReleasesByYear = year => {
  return newReleasesChannels.filter(channel =>
    channel.tags.some(tag => tag.includes(year.toString()))
  );
};

export const getFrancophoneNewReleases = () => {
  return newReleasesChannels.filter(channel =>
    channel.tags.some(tag => ['French', 'Francophone', 'Quebec', 'Québécois'].includes(tag))
  );
};

export const getBestOf2025Channels = () => {
  return newReleasesChannels.filter(
    channel => channel.title.includes('Best of 2025') || channel.title.includes('succès de 2025')
  );
};

export const getNewReleasesByPlayCount = (limit = 10) => {
  return [...newReleasesChannels].sort((a, b) => b.playCount - a.playCount).slice(0, limit);
};

// Get all new releases (all channels in this dataset are marked as new)
export const getAllNewReleases = () => {
  return newReleasesChannels;
};
