// Helper functions for easier data manipulation
export const getRecommendationsByGenre = genre => {
  return recommendationsChannels.filter(
    channel => channel.genre.toLowerCase() === genre.toLowerCase()
  );
};

export const getRetroRecommendations = () => {
  return recommendationsChannels.filter(channel =>
    channel.tags.some(tag => ['1970s', '1980s', '1990s', 'Retro', 'Nostalgic'].includes(tag))
  );
};

export const getWorkoutRecommendations = () => {
  return recommendationsChannels.filter(channel =>
    channel.tags.some(tag => ['Workout', 'High Energy', 'Party', 'Energetic'].includes(tag))
  );
};

export const getTeenPopRecommendations = () => {
  return recommendationsChannels.filter(channel =>
    channel.tags.some(tag => ['Teen Pop', 'Boy Bands', 'Teen Beat', 'Youthful'].includes(tag))
  );
};

export const getTrendingRecommendations = () => {
  return recommendationsChannels.filter(channel =>
    channel.tags.some(tag => ['Viral', 'TikTok', 'Trending', 'Social Media'].includes(tag))
  );
};

export const getRecommendationsByPlayCount = (limit = 10) => {
  return [...recommendationsChannels].sort((a, b) => b.playCount - a.playCount).slice(0, limit);
};

// Get all recommendations
export const getAllRecommendations = () => {
  return recommendationsChannels;
};
