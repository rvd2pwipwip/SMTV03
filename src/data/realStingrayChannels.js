// Helper functions for easier data manipulation
export const getChannelsByGenre = genre => {
  return stingrayChannels.filter(channel => channel.genre.toLowerCase() === genre.toLowerCase());
};

export const getPopularChannels = () => {
  return stingrayChannels.filter(channel => channel.isPopular);
};

export const getNewChannels = () => {
  return stingrayChannels.filter(channel => channel.isNew);
};

export const getChannelsByTag = tag => {
  return stingrayChannels.filter(channel =>
    channel.tags.some(t => t.toLowerCase().includes(tag.toLowerCase()))
  );
};

export const getTopChannelsByPlays = (limit = 10) => {
  return [...stingrayChannels].sort((a, b) => b.playCount - a.playCount).slice(0, limit);
};

// Get Most Popular channels (all channels in this dataset are marked as popular)
export const getMostPopularChannels = () => {
  return getPopularChannels();
};
